<?php
/**
 * ++$a pre-incremento, incrementa $a em um e retorna $a
 * $a++ pos-incremento, retorna $a e incrementa $a
 * --$a contrario de ++$a
 * $a-- contrario de $a++
 */

 $a = 10;

 echo ++$a * 2;
 echo "<br>";
 echo $a++ * 2;
 echo "<br>";
 echo --$a * 2;
 echo "<br>";
 echo $a-- * 2;
 echo "<br>";